/** Get species by category */
export function getSpeciesByCategory(category: SpeciesData['category']): SpeciesData[] {
  return Object.values(SPECIES).filter(s => s.category === category);
}

/** Get a random species */
export function getRandomSpecies(): SpeciesData {
  const all = Object.values(SPECIES);
  return all[Math.floor(Math.random() * all.length)];
}
